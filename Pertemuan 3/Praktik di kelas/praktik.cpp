#include <iostream>
using namespace std;

int main () {
    int a = 10, b = 2 ;
    bool c = true, d = false ;

    cout << "A + B = "<< a + b << endl;
    cout << "A - B = "<< a - b << endl;
    cout << "A * B = "<< a * b << endl;
    cout << "A / B = "<< a / b << endl;
    cout << "A % B = "<< a % b << endl;
    cout << "A > B = "<< (a > b) << endl;
    cout << "A < B = "<< (a < b) << endl;
    cout << "A == B = "<< (a == b) << endl;
    cout << "A >= B = "<< (a >= b) << endl;
    cout << "A <= B = "<< (a <= b) << endl;
    cout << "A != B = "<< (a != b) << endl;
    cout << "c && d = "<< (c && d) << endl;
    cout << "c || d = "<< (c || d) << endl;
    cout << "c! = "<< (!c) << endl;


   

    return 0;
}
















